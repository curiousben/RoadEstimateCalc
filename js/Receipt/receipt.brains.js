function numberleftblank (num) {
    if (num) {
        return num;
    } else{
        return 0;
    };
}

function numberleftblankcomma (num) {
    if (num) {
        return num.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
    }
    else {
        return 0;
    }
}

function textleftblank (Tex) {
    if (Tex) 
    {
        return Tex;
    }
    else {
        return 'Not Entered';
    }
}